package com.ing.zoo.herbivores;

/**
 * Represents a herbivorous animal capable of eating leaves.
 * Implementing classes should define specific behavior for consuming leaves.
 */
public interface Herbivore {

    /**
     * Defines how the herbivore eats leaves.
     * This method should be implemented by any class that represents a herbivore.
     */
    void eatLeaves();
}
