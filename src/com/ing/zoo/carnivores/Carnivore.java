package com.ing.zoo.carnivores;

/**
 * Represents a carnivorous animal capable of eating meat.
 * Implementing classes should define specific behavior for consuming meat.
 */
public interface Carnivore {

    /**
     * Defines how the carnivore eats meat.
     * This method should be implemented by any class that represents a carnivore.
     */
    void eatMeat();
}
